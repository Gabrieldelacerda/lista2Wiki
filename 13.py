numero_dia = int(input("Digite um número correspondente ao dia da semana (1 a 7): "))
if numero_dia == 1:
    print("Domingo")
elif numero_dia == 2:
    print("Segunda")
elif numero_dia ==3:
    print("Terça-feira")
elif numero_dia == 4:
    print("Quarta")
elif numero_dia == 5:
    print("Quinta")
elif numero_dia == 6:
    print("Sexta")
elif numero_dia == 7:
    print("Sabado")